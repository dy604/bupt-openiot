package bishe;

public class AHP {

	/**
	 * @param args
	 */
	private int at;
	private double bt;
	private int ct;
	private float dt;


	public double getBt() {
		return bt;
	}

	public void setBt(double d) {
		this.bt = d;
	}

	public int getAt() {
		return at;
	}

	public void setAt(int at) {
		this.at = at;
	}

	public int getCt() {
		return ct;
	}

	public void setCt(int ct) {
		this.ct = ct;
	}

	public float getDt() {
		return dt;
	}

	public void setDt(float dt) {
		this.dt = dt;
	}

}
